export function maturityFor(component: FoundationComponent): MaturityLevel {
  return MATURITY_FROM_STATUS[component.status];
}

export function groupFor(component: FoundationComponent): FoundationGroupDef {
  const group = FOUNDATION_GROUPS.find((g) => g.id === component.groupId);
  if (!group) throw new Error(`Foundation catalog: unknown group id "${component.groupId}"`);
  return group;
}

export interface CatalogSummary {
  total: number;
  byStatus: Record<InventoryStatus, number>;
  byMaturity: Record<MaturityLevel, number>;
  highPriorityGaps: FoundationComponent[];
}

/** Every number here is computed live from FOUNDATION_COMPONENTS — nothing in this summary is hand-typed. */
export function catalogSummary(): CatalogSummary {
  const byStatus: Record<InventoryStatus, number> = { Exists: 0, Partial: 0, Needed: 0 };
  const byMaturity: Record<MaturityLevel, number> = {
    Concept: 0,
    Prototype: 0,
    "Production Ready": 0,
    Certified: 0,
    Locked: 0,
  };
  for (const component of FOUNDATION_COMPONENTS) {
    byStatus[component.status] += 1;
    byMaturity[maturityFor(component)] += 1;
  }
  const highPriorityGaps = FOUNDATION_COMPONENTS.filter(
    (component) => component.priority === "High" && component.status !== "Exists",
  );
  return { total: FOUNDATION_COMPONENTS.length, byStatus, byMaturity, highPriorityGaps };
}
