export type FoundationGroupId =
  | "actions"
  | "inputs"
  | "feedback"
  | "overlays"
  | "navigation"
  | "data-display"
  | "layout-primitives"
  | "operational"
  | "workflow"
  | "platform";

export interface FoundationGroupDef {
  id: FoundationGroupId;
  title: string;
  description: string;
}

