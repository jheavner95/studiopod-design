import { INVENTORY_GROUPS, type InventoryStatus, type InventoryPriority } from "../../inventory/_data/inventory";
import { MATURITY_FROM_STATUS, type MaturityLevel } from "../../_data/maturity";

export type FoundationGroupId =
  | "actions"
  | "inputs"
  | "feedback"
  | "overlays"
  | "navigation"
  | "data-display"
  | "layout-primitives";

export interface FoundationGroupDef {
  id: FoundationGroupId;
  title: string;
  description: string;
}

export const FOUNDATION_GROUPS: FoundationGroupDef[] = [
  { id: "actions", title: "Actions", description: "Buttons and other triggers a user clicks to do something." },
  { id: "inputs", title: "Inputs", description: "Form controls for entering or selecting a value." },
  { id: "feedback", title: "Feedback", description: "Communicating state, status, and outcome back to the user." },
  { id: "overlays", title: "Overlays", description: "Content that floats above the page on demand, dismissed explicitly." },
  { id: "navigation", title: "Navigation", description: "Moving between views, pages, sections, or steps." },
  { id: "data-display", title: "Data Display", description: "Presenting structured content and information at rest." },
  { id: "layout-primitives", title: "Layout Primitives", description: "The composition building blocks every other component is arranged with." },
];

export interface FoundationComponent {
  id: string;
  name: string;
  groupId: FoundationGroupId;
  purpose: string;
  status: InventoryStatus;
  source?: string;
  requiredStates: string[];
  requiredVariants: string[];
  accessibility: string[];
  reuseTargets: string[];
  priority: InventoryPriority;
  /** Set only when this entry's status/source/priority was looked up from the DS-0.2 inventory rather than derived fresh from the codebase. */
  derivedFrom?: string;
}

const ALL_INVENTORY_ITEMS = INVENTORY_GROUPS.flatMap((group) => group.items);

function fromInventory(name: string) {
  const item = ALL_INVENTORY_ITEMS.find((i) => i.name === name);
  if (!item) throw new Error(`Foundation catalog references unknown inventory item: ${name}`);
  return item;
}

